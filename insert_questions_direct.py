def insert_questions():
    print("开始生成2000道题目（均衡分布：每种500道）...")

    conn = pymysql.connect(host=DB_HOST, user=DB_USER, password=DB_PASSWORD, database=DB_NAME, charset=DB_CHARSET)
    cursor = conn.cursor()

    questions = []

    single_templates = generate_single_choice_templates()
    fill_templates = generate_fill_blank_templates()
    multi_templates = generate_multi_choice_templates()
    program_templates = generate_programming_templates()

    print(f"单选模板: {len(single_templates)}, 填空模板: {len(fill_templates)}, 多选模板: {len(multi_templates)}, 编程模板: {len(program_templates)}")

    opt_4 = '{"A":"选项A","B":"选项B","C":"选项C","D":"选项D"}'
    opt_5 = '{"A":"选项A","B":"选项B","C":"选项C","D":"选项D","E":"选项E"}'

    # 生成500道单选
    for i in range(500):
        template = single_templates[i % len(single_templates)]
        q_text, ans, anal = template
        questions.append(q(2, q_text + f" [题{i+1}]", ans, opt_4, anal))

    # 生成500道填空
    for i in range(500):
        template = fill_templates[i % len(fill_templates)]
        q_text, ans = template
        questions.append(q(1, q_text + f" [题{i+1}]", ans))

    # 生成500道多选
    for i in range(500):
        template = multi_templates[i % len(multi_templates)]
        q_text, ans, anal = template
        questions.append(q(3, q_text + f" [题{i+1}]", ans, opt_5, anal))

    # 生成500道编程
    for i in range(500):
        template = program_templates[i % len(program_templates)]
        q_text, answer, cases_str = template

        test_cases = []
        case_list = cases_str.split(",")
        for j, case in enumerate(case_list):
            parts = case.split("->")
            inp = parts[0]
            expected = parts[1] if len(parts) > 1 else ""
            test_cases.append({
                "input": inp,
                "expected_output": expected,
                "score_weight": 25,
                "sort_index": j
            })

        questions.append(q_program(q_text + f" [题{i+1}]", answer, test_cases))

    print(f"\n生成了 {len(questions)} 道题目")

    # Insert questions directly
    for i, q_item in enumerate(questions):
        question_text = q_item["question"]
        answer = q_item.get("answer", "")
        options = q_item.get("options", "")
        analysis = q_item.get("analysis", "")
        qtype = q_item.get("type", 2)
        score = q_item.get("score", 10)
        is_prog = q_item.get("is_programming", 0)
        lang = q_item.get("language", "java")
        template = q_item.get("template_code", "")
        io_mode = q_item.get("io_mode", "standard")

        if is_prog:
            sql = """INSERT INTO t_question (question, answer, type, score, options, analysis, is_programming, language, template_code, io_mode, teacher_id, state)
VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, 2, 1)"""
            cursor.execute(sql, (question_text, answer, qtype, score, options, analysis, is_prog, lang, template, io_mode))
        else:
            sql = """INSERT INTO t_question (question, answer, type, score, options, analysis, teacher_id, state)
VALUES (%s, %s, %s, %s, %s, %s, 2, 1)"""
            cursor.execute(sql, (question_text, answer, qtype, score, options, analysis))

        question_id = cursor.lastrowid

        # Insert test cases for programming questions
        if "test_cases" in q_item and q_item["test_cases"]:
            for tc in q_item["test_cases"]:
                tc_sql = """INSERT INTO t_question_test_case (question_id, input, expected_output, score_weight, sort_index)
VALUES (%s, %s, %s, %s, %s)"""
                cursor.execute(tc_sql, (question_id, tc["input"], tc["expected_output"], tc["score_weight"], tc["sort_index"]))

        if (i + 1) % 100 == 0:
            print(f"已插入 {i + 1} 道题目...")
            conn.commit()

    conn.commit()

    # Verify
    cursor.execute("SELECT COUNT(*) FROM t_question")
    total = cursor.fetchone()[0]
    print(f"\n数据库中共有 {total} 道题目")

    cursor.execute("SELECT type, COUNT(*) as count FROM t_question GROUP BY type")
    print("\n题目类型分布：")
    for row in cursor.fetchall():
        type_names = {1: "填空", 2: "单选", 3: "多选", 5: "简答", 6: "编程"}
        print(f"  {type_names.get(row[0], row[0])}: {row[1]} 道")

    cursor.execute("SELECT COUNT(*) FROM t_question_test_case")
    tc_count = cursor.fetchone()[0]
    print(f"\n编程题测试用例: {tc_count} 条")

    conn.close()
    print("\n完成!")


